function normalize(text: string) {
  return text
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[!?.,:;()"]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function answerFromKnowledgeBase(question: string) {
  const normalized = normalize(question);

  const scored = assistantKnowledge
    .map((entry) => {
      const score = entry.keywords.reduce((sum, keyword) => sum + (normalized.includes(normalize(keyword)) ? 1 : 0), 0);
      return { entry, score };
    })
    .sort((a, b) => b.score - a.score);

  if (!scored[0] || scored[0].score === 0) {
    return null;
  }

  return scored[0].entry.content;
}
