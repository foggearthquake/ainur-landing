function normalize(text: string) {
  return text
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[!?.,:;()"]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function answerFromKeywords(question: string): string {
  const normalized = normalize(question);

  const scored = keywordEntries
    .map((entry) => {
      const score = entry.keywords.reduce(
        (sum, keyword) => sum + (normalized.includes(normalize(keyword)) ? 1 : 0),
        0,
      );
      return { entry, score };
    })
    .sort((a, b) => b.score - a.score);

  if (!scored[0] || scored[0].score === 0) {
    return "Хороший вопрос! Мы делаем любые цифровые решения — от сайтов до платформ и AI-систем. Оставь заявку — разберём задачу и предложу решение за 3–7 дней.";
  }

  return scored[0].entry.content;
}

